const initGallerySliders = () => {
  document.querySelectorAll('.gallery-slider-container').forEach((container) => {
    const slider = container.querySelector('.gallery-slider');
    const items = Array.from(slider.querySelectorAll('.gallery-item'));
    if (!slider || items.length === 0) return;

    const itemWidth = items[0].offsetWidth + 12;
    container.querySelector('.gallery-prev').addEventListener('click', () => {
      slider.scrollBy({ left: -itemWidth, behavior: 'smooth' });
    });
    container.querySelector('.gallery-next').addEventListener('click', () => {
      slider.scrollBy({ left: itemWidth, behavior: 'smooth' });
    });

    slider.addEventListener('wheel', (event) => {
      if (Math.abs(event.deltaX) > Math.abs(event.deltaY)) {
        event.preventDefault();
        slider.scrollBy({ left: event.deltaX, behavior: 'smooth' });
      }
    }, { passive: false });
  });
};

const initGalleryObservers = () => {
  document.querySelectorAll('.gallery-slider').forEach((slider) => {
    const items = Array.from(slider.querySelectorAll('.gallery-item'));
    if (!items.length) return;

    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          entry.target.classList.toggle('active', entry.intersectionRatio >= 0.55);
        });
      },
      {
        root: slider,
        threshold: [0.55]
      }
    );

    items.forEach((item) => observer.observe(item));
  });
};

const observeActiveSlide = () => {
  const slides = document.querySelectorAll('.story-slide');
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        const isActive = entry.isIntersecting;
        entry.target.classList.toggle('active', isActive);
      });
    },
    { threshold: 0.55 }
  );
  slides.forEach((slide) => observer.observe(slide));
};

const initSlideActions = () => {
  document.querySelectorAll('.btn-primary').forEach((button) => {
    button.addEventListener('click', (event) => {
      const targetId = event.currentTarget.dataset.target;
      const item = STORY_DATA.find((entry) => entry.id === targetId);
      if (!item) return;
      if (item.coords) {
        const mapsUrl = `https://www.google.com/maps/search/?api=1&query=${item.coords[0]},${item.coords[1]}`;
        window.open(mapsUrl, '_blank');
      } else if (item.peta) {
        openMapModal(item.peta, item.name);
      } else {
        alert(`Tidak ada data lokasi untuk ${item.name}.`);
      }
    });
  });

  const fixedHome = document.getElementById('fixed-home-button');
  if (fixedHome) {
    fixedHome.addEventListener('click', () => {
      window.location.href = 'index.html';
    });
  }

  document.querySelectorAll('.gallery-item img').forEach((image) => {
    image.addEventListener('click', (event) => {
      const src = event.currentTarget.dataset.src;
      const alt = event.currentTarget.alt;
      if (src) {
        openMapModal(src, alt);
      }
    });
  });

  // Map preview slider navigation (peta <-> topografi)
  document.querySelectorAll('.map-preview').forEach((preview) => {
    const slider = preview.querySelector('.map-slider');
    if (!slider) return;
    const items = Array.from(slider.querySelectorAll('.map-item'));
    const prevBtn = preview.querySelector('.map-prev');
    const nextBtn = preview.querySelector('.map-next');

    const updateActive = (index) => {
      items.forEach((it, i) => it.classList.toggle('active', i === index));
    };

    let current = 0;

    // Try to validate topo image; if it fails, hide nav buttons
    const topoImg = slider.querySelector('.map-item:nth-child(2) img');
    if (topoImg) {
      const primaryTopo = topoImg.getAttribute('src');
      const fallbackTopo = topoImg.dataset.fallback;
      const check = new Image();
      check.onload = () => {
        // topo exists, enable nav
      };
      check.onerror = () => {
        if (fallbackTopo) {
          const check2 = new Image();
          check2.onload = () => {
            topoImg.src = fallbackTopo;
          };
          check2.onerror = () => {
            if (prevBtn) prevBtn.style.display = 'none';
            if (nextBtn) nextBtn.style.display = 'none';
            topoImg.style.display = 'none';
          };
          check2.src = fallbackTopo;
        } else {
          if (prevBtn) prevBtn.style.display = 'none';
          if (nextBtn) nextBtn.style.display = 'none';
          topoImg.style.display = 'none';
        }
      };
      check.src = primaryTopo;
    } else {
      if (prevBtn) prevBtn.style.display = 'none';
      if (nextBtn) nextBtn.style.display = 'none';
    }

    if (prevBtn) prevBtn.addEventListener('click', (e) => {
      e.stopPropagation();
      current = Math.max(0, current - 1);
      updateActive(current);
    });
    if (nextBtn) nextBtn.addEventListener('click', (e) => {
      e.stopPropagation();
      current = Math.min(items.length - 1, current + 1);
      updateActive(current);
    });

    // Open modal for currently visible image when clicking preview area
    preview.addEventListener('click', (e) => {
      // ignore clicks on nav buttons
      if (e.target.closest('.map-nav')) return;
      const activeImg = preview.querySelector('.map-item.active img');
      const name = preview.dataset.name || '';
      if (activeImg && activeImg.src) openMapModal(activeImg.src, name || activeImg.alt || 'Peta');
    });
  });
};

const tryLoadImageThenOpen = (primary, fallback, name) => {
  if (!primary) {
    alert('Topografi tidak tersedia untuk lokasi ini.');
    return;
  }
  const img = new Image();
  img.onload = () => openMapModal(primary, name || 'Topografi');
  img.onerror = () => {
    if (fallback && fallback !== primary) {
      const img2 = new Image();
      img2.onload = () => openMapModal(fallback, name || 'Topografi');
      img2.onerror = () => alert('Gambar topografi tidak ditemukan.');
      img2.src = fallback;
    } else {
      alert('Gambar topografi tidak ditemukan.');
    }
  };
  img.src = primary;
};

const openMapModal = (src, name) => {
  let modal = document.getElementById('map-modal');
  if (!modal) {
    modal = document.createElement('div');
    modal.id = 'map-modal';
    modal.innerHTML = `
      <div class="map-modal-backdrop"></div>
      <div class="map-modal-card">
        <button class="map-modal-close" aria-label="Close">×</button>
        <div class="map-modal-title">${name}</div>
        <img class="map-modal-image" src="${src}" alt="Gambar ${name}" />
        <a class="map-modal-link" href="${src}" target="_blank">Buka gambar penuh</a>
      </div>
    `;
    document.body.appendChild(modal);
    modal.querySelector('.map-modal-close').addEventListener('click', () => modal.classList.remove('active'));
    modal.querySelector('.map-modal-backdrop').addEventListener('click', () => modal.classList.remove('active'));
  } else {
    modal.querySelector('.map-modal-title').textContent = name;
    modal.querySelector('.map-modal-image').src = src;
    modal.querySelector('.map-modal-link').href = src;
  }
  modal.classList.add('active');
};

const loadAndBuild = async () => {
  try {
    const res = await fetch('data/terapan.json');
    if (res.ok) {
      const data = await res.json();
      if (Array.isArray(data) && data.length) {
        STORY_DATA = data;
      }
    }
  } catch (e) {
    // ignore and use embedded STORY_DATA
  }
  buildSlides();
};

window.addEventListener('DOMContentLoaded', loadAndBuild);
